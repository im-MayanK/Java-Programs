public class typecasting {
    public static void main(String[] args) {
        int a=45;
        double b;
      b=a;
      System.out.println(b);//implicit typecasting
      double num1 =45.5;
      int num2;
      num2= (int) num1;
      System.out.println(num2);//explicit typecasting

    }
    
}
